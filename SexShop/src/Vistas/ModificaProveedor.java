    public ModificaProveedor(java.awt.Frame parent, boolean modal, int idproveedor) {
        super(parent, modal);
        this.setTitle("Modificar Proveedor");
        initComponents();
        this.idproveedor = idproveedor;
        rbgEstado.add(rbActivo);
        rbgEstado.add(rbNoActivo);
        try {
            modificado = ctrlproveedores.traerPorId(idproveedor);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "MySql", JOptionPane.ERROR_MESSAGE);
        }
        if (modificado != null) {
            txtNombre.setText(modificado.getRazonsocial());
            txtDireccion.setText(modificado.getDireccion());
            txtTelefono.setText(modificado.getTelefono());
            txtMail.setText(modificado.getMail());
            if (modificado.getEstado().getId() == 1) {
                rbActivo.setSelected(true);
            } else {
                rbNoActivo.setSelected(true);
            }
        }
    }

