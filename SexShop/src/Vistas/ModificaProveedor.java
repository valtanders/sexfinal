    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int estado;
        String desc;
        if (rbActivo.isSelected()) {
            estado = 1;
            desc = "ACTIVO";
        } else {
            estado = 2;
            desc = "INACTIVO";
        }
        try {
            this.setModificado(ctrlproveedores.modificar(new Proveedor(idproveedor,txtNombre.getText(), txtDireccion.getText(), txtTelefono.getText(), txtMail.getText(), new Estado(estado, desc))));
            this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "MySql", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

